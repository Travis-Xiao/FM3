        /// <summary>
        /// Convert double directly to string and truncate the fraction part.
        /// </summary>
        /// <param name="d"></param>
        /// <param name="radix"></param>
        /// <returns></returns>
        public static string ToString(double d, int radix)
        {
            double o = d;
            if (radix != 2)
            {
                throw new Exception("not supported");
            }
            if (double.IsInfinity(d))
            {
                throw new Exception("Infinity");
            }
            var b = new StringBuilder();

            while (d >= 2)
            {
                b.Insert(0, d % 2 >= 1 ? '1' : '0');
                d /= radix;
            }
            b.Insert(0, d >= 1 ? '1' : '0');
            return b.ToString();
        }
